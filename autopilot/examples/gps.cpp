class MyGPSSensor final : public AP::GPSSensor
{
public:
  MyGPSSensor(std::string data)
    : data_(std::move(data))
  {
  }

  [[nodiscard]] auto read() -> bool override
  {
    while (true) {
      const auto c = readData();
      if (c < 0) {
        break;
      }
      char buf{ static_cast<char>(c) };
      if (parseData(&buf, 1)) {
        return true;
      }
    }
    return false;
  }

protected:
  [[nodiscard]] auto readData() -> int
  {
    if (offset_ >= data_.size()) {
      return -1;
    }

    const auto c = data_.at(offset_);

    offset_++;

    return static_cast<int>(c);
  }

private:
  std::string data_;

  size_t offset_{};
};

